+ (NSString *)dateStringFromTimeStamp:(NSString *)timestamp{
    // 时间处理
    NSInteger dateInteger = [timestamp integerValue] / 1000;
    NSDate *date = [[NSDate alloc]initWithTimeIntervalSince1970:dateInteger];
    // 若需要显示到'秒'，则截取范围为（0，19）
    NSString *dateStr = [[NSString stringWithFormat:@"%@",date] substringWithRange:NSMakeRange(0, 10)];
    
    return dateStr;
}

+ (NSString *)contentTypeForImageData:(NSData *)data{
    uint8_t c;
    [data getBytes:&c length:1];
    switch (c) {
        case 0xFF:
        return @"jpeg";
        case 0x89:
        return @"png";
        case 0x47:
        return @"gif";
        case 0x49:
        case 0x4D:
        return @"tiff";
        case 0x52:
        if ([data length] < 12) {
            return nil;
        }
        NSString *testString = [[NSString alloc] initWithData:[data subdataWithRange:NSMakeRange(0, 12)] encoding:NSASCIIStringEncoding];
        if ([testString hasPrefix:@"RIFF"] && [testString hasSuffix:@"WEBP"]) {
            return @"webp";
        }
        return nil;
    }
    return nil;
}

